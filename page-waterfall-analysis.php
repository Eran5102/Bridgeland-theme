        <div class="row g-4">
            <div class="col-lg-6">
                <div class="preferred-card card border-0 shadow-sm h-100">
                    <div class="card-body p-4">
                        <h5 class="mb-3 text-primary">
                            <i class="fas fa-shield-alt me-2"></i>
                            Non-Participating Preferred
                        </h5>
                        <p class="text-muted mb-3">
                            Holders receive liquidation preference OR convert to common stock,
                            whichever provides greater return. Cannot participate in both.
                        </p>
                        <div class="example-box bg-light p-3 rounded">
                            <strong>Example:</strong> $2M investment with 1x liquidation preference.
                            In $10M exit, converts to common for higher return than $2M preference.
                        </div>
                    </div>
                </div>
            </div>

            <div class="col-lg-6">
                <div class="preferred-card card border-0 shadow-sm h-100">
                    <div class="card-body p-4">
                        <h5 class="mb-3 text-primary">
                            <i class="fas fa-plus-circle me-2"></i>
                            Participating Preferred
                        </h5>
                        <p class="text-muted mb-3">
                            Holders receive liquidation preference AND participate in remaining
                            proceeds on as-converted basis. "Double-dip" protection.
                        </p>
                        <div class="example-box bg-light p-3 rounded">
                            <strong>Example:</strong> $2M investment gets $2M preference PLUS
                            pro-rata share of remaining $8M in $10M exit.
                        </div>
                    </div>
                </div>
            </div>

            <div class="col-lg-6">
                <div class="preferred-card card border-0 shadow-sm h-100">
                    <div class="card-body p-4">
                        <h5 class="mb-3 text-primary">
                            <i class="fas fa-stopwatch me-2"></i>
                            Capped Participating
                        </h5>
                        <p class="text-muted mb-3">
                            Participating preferred with maximum return cap, typically 2-3x
                            the original investment amount.
                        </p>
                        <div class="example-box bg-light p-3 rounded">
                            <strong>Example:</strong> $2M investment with 3x cap receives maximum
                            $6M regardless of total exit value above that threshold.
                        </div>
                    </div>
                </div>
            </div>

            <div class="col-lg-6">
                <div class="preferred-card card border-0 shadow-sm h-100">
                    <div class="card-body p-4">
                        <h5 class="mb-3 text-primary">
                            <i class="fas fa-layer-group me-2"></i>
                            Senior Preferred
                        </h5>
                        <p class="text-muted mb-3">
                            Higher priority in liquidation waterfall. Gets paid before
                            junior preferred classes and common stock.
                        </p>
                        <div class="example-box bg-light p-3 rounded">
                            <strong>Example:</strong> Series B gets liquidation preference before
                            Series A, providing additional downside protection.
                        </div>
                    </div>
                </div>
            </div>
        </div>
