                    <h4 class="h5 mb-3">Cohort-Based Revenue Modeling</h4>
                    <p>
                        Instead of simple top-down growth rates, model revenue by customer cohorts to capture
                        the nuanced behavior of subscription businesses and their evolution over time.
                    </p>

                    <div class="bg-info bg-opacity-10 p-4 rounded my-4">
                        <h4 class="h5 text-info mb-2">Cohort Model Components:</h4>
                        <ul class="mb-0">
                            <li><strong>Acquisition Cohorts:</strong> Group customers by acquisition month/quarter</li>
                            <li><strong>Retention Curves:</strong> Model churn patterns for each cohort</li>
                            <li><strong>Expansion Revenue:</strong> Track upselling and cross-selling within cohorts</li>
                            <li><strong>Cohort Maturation:</strong> Account for changing behavior as cohorts age</li>
                        </ul>
                    </div>

